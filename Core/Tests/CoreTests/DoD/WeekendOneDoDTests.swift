private enum DoDFixtures {

    static func makeEnv() async throws -> DoDEnv {
        let root = try makeTmpDir()
        let libraryRoot = root.appendingPathComponent("library", isDirectory: true)
        let witnessesRoot = root.appendingPathComponent("witnesses", isDirectory: true)
        try FileManager.default.createDirectory(at: libraryRoot, withIntermediateDirectories: true)

        let index = try LibraryIndex(databaseURL: root.appendingPathComponent("index.db"))

        let selector = StubRegionSelecting(canned: selection())
        let capturer = StubScreenCapturing(frame: capturedFrame())
        let coordinator = CaptureCoordinator(
            regionSelector: selector,
            screenCapturer: capturer,
            finalizer: RealFinalizing(),
            indexer: RealLibraryIndexing(index: index),
            libraryRoot: libraryRoot,
            witnessesRoot: witnessesRoot,
            contextProvider: {
                CaptureFinalization.Context(
                    frontmostBundleID: "com.example.dod",
                    frontmostWindowTitle: "DoD Test Window",
                    axAvailable: false
                )
            }
        )

        return DoDEnv(
            root: root,
            libraryRoot: libraryRoot,
            witnessesRoot: witnessesRoot,
            index: index,
            coordinator: coordinator
        )
    }

    static func makeTmpDir() throws -> URL {
        let url = FileManager.default.temporaryDirectory
            .appendingPathComponent("dod-\(UUID().uuidString)", isDirectory: true)
        try FileManager.default.createDirectory(at: url, withIntermediateDirectories: true)
        return url
    }

    static func cleanup(_ url: URL) {
        try? FileManager.default.removeItem(at: url)
    }

    static func clearPasteboard() {
        NSPasteboard.general.clearContents()
    }

    // MARK: - CapturedFrame builders

    static func display(id: CGDirectDisplayID = 1) -> DisplayMetadata {
        DisplayMetadata(
            id: id,
            nativeWidth: 3024,
            nativeHeight: 1964,
            nativeScale: 2.0,
            vendorID: nil,
            productID: nil,
            serial: nil,
            localizedName: "DoD Test",
            globalFrame: CGRect(x: 0, y: 0, width: 1512, height: 982)
        )
    }

    static func selection() -> RegionSelection {
        RegionSelection(
            rect: CGRect(x: 0, y: 0, width: 320, height: 200),
            display: display()
        )
    }

    /// 320×200 solid-grey CGImage. Realistic enough that PNG/JPEG encoding
    /// produces non-trivial bytes, small enough that 30 captures finish
    /// well under the warm budget.
    static func solidImage(width: Int = 320, height: Int = 200) -> CGImage {
        let bytesPerRow = width * 4
        var pixels = [UInt8](repeating: 0x7F, count: bytesPerRow * height)
        let cs = CGColorSpace(name: CGColorSpace.sRGB)!
        let ctx = pixels.withUnsafeMutableBufferPointer { buf -> CGContext in
            CGContext(
                data: buf.baseAddress,
                width: width,
                height: height,
                bitsPerComponent: 8,
                bytesPerRow: bytesPerRow,
                space: cs,
                bitmapInfo: CGImageAlphaInfo.premultipliedLast.rawValue
            )!
        }
        return ctx.makeImage()!
    }

    static func capturedFrame() -> CapturedFrame {
        CapturedFrame(
            image: solidImage(),
            pixelBounds: CGRect(x: 0, y: 0, width: 320, height: 200),
            display: display(),
            capturedAt: Date()
        )
    }

    // MARK: - Fake .shot/ package writer (for fuse-gc test)

    /// Writes a minimal `.shot/` directory with just a manifest carrying
    /// `expires_at` and `pinned`. FusePolicy only consults those two
    /// fields (Core/Sources/Core/Fuse/FusePolicy.swift:58-60), so the
    /// missing master.png/ocr.json/etc. don't matter for this test.
    /// Validates a UUIDv7 string per RFC 9562 §5.7: 8-4-4-4-12 lowercase hex
    /// with version nibble = 7 in the third group's first character and
    /// variant bits = 10 (high bits of the fourth group's first nibble in
    /// {8, 9, a, b}).
    static func isValidUUIDv7(_ s: String) -> Bool {
        guard UUID(uuidString: s) != nil else { return false }
        let parts = s.lowercased().split(separator: "-")
        guard parts.count == 5 else { return false }
        guard parts[2].first == "7" else { return false }
        let variant = parts[3].first.map(String.init) ?? ""
        return ["8", "9", "a", "b"].contains(variant)
    }

    static func writeFakeShotPackage(at url: URL, expiresAt: Date, pinned: Bool) throws {
        let fm = FileManager.default
        try fm.createDirectory(at: url, withIntermediateDirectories: false)
        let manifest: [String: Any] = [
            "id": UUID().uuidString,
            "expires_at": expiresAt.formatted(.iso8601),
            "pinned": pinned,
        ]
        let data = try JSONSerialization.data(withJSONObject: manifest, options: [.sortedKeys])
        try data.write(to: url.appendingPathComponent("manifest.json"))
    }

    // MARK: - Latency math

    static func seconds(_ d: Duration) -> TimeInterval {
        let comps = d.components
        return TimeInterval(comps.seconds) + TimeInterval(comps.attoseconds) / 1e18
    }

    /// Linear-interpolation percentile on a pre-sorted ascending array.
    /// Matches the "nearest-rank with interpolation" definition used in
    /// most monitoring tools (Prometheus quantile, Grafana p95, etc).
    static func percentile(_ p: Double, ofSorted samples: [TimeInterval]) -> TimeInterval {
        precondition(!samples.isEmpty)
        if samples.count == 1 { return samples[0] }
        let rank = p * Double(samples.count - 1)
        let lo = Int(rank.rounded(.down))
        let hi = Int(rank.rounded(.up))
        if lo == hi { return samples[lo] }
        let frac = rank - Double(lo)
        return samples[lo] + (samples[hi] - samples[lo]) * frac
    }
}

